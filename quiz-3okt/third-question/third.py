first = int(input("Masukkan angka pertama : "))
second = int(input("Masukkan angka kedua : "))

print('Hasil dari', first, '+', second, 'adalah', first + second)
print('Hasil dari', first, '-', second, 'adalah', first - second)
print('Hasil dari', first, '/', second, 'adalah', first / second)
print('Hasil dari', first, '*', second, 'adalah', first * second)
